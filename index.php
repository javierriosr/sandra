    const scrollToTopBtn = document.getElementById('scrollToTopBtn');
    window.addEventListener('scroll', () => {
        if (window.pageYOffset > 300) {
            scrollToTopBtn.style.display = 'block';
        } else {
            scrollToTopBtn.style.display = 'none';
        }
    });
    scrollToTopBtn.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });

    // 2. Animación de aparición al hacer scroll (Intersection Observer)
    // Es más eficiente que el método anterior.
    const revealElements = document.querySelectorAll('.reveal-on-scroll');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry
                    .target); // Opcional: deja de observar el elemento una vez que es visible
            }
        });
    }, {
        threshold: 0.1 // El elemento se revela cuando el 10% es visible
    });

    revealElements.forEach(element => {
        observer.observe(element);
    });
